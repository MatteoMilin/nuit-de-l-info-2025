export function generateRandomDeck() {
  const shuffled = [...ALL_CARDS].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, 8).map(card => ({
    name: card.name,
    id: card.id,
    rarity: card.rarity,
    iconUrl: `https://api-assets.clashroyale.com/cards/300/${card.name.replace(/\s+/g, '-').replace(/\./g, '').toLowerCase()}.png`
  }));
}
